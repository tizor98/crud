package com.message.model;

public class Author {

   private int id;
   private String name;

   public Author() {

   }

   public String getName() { return this.name; }

   public void setName(String name) {
      this.name = name;
   }

   public int getId() {
      return id;
   }

   public void setId(int id) {
      this.id = id;
   }
}
